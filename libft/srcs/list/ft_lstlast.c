#include "ft_list.h"

/* ************************************************************************** */
/*  This function returns the last element of the list.                       */
/* ************************************************************************** */
t_list	*ft_lstlast(t_list *lst)
{
	if (lst == NULL)
		return (NULL);
	while (lst->next != NULL)
		lst = lst->next;
	return (lst);
}
