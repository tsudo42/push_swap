/* ************************************************************************** */
/* ************************************************************************** */
int	ft_index(int key, int *arr, int size)
{
	int	i;

	i = 0;
	while (i < size)
	{
		if (key == arr[i])
			return (i);
		i++;
	}
	return (-1);
}
