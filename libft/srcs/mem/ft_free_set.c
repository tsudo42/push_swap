#include "ft_mem.h"

/* ************************************************************************** */
/*  This function frees '*ptr' and after that '*ptr' is set to 'set'          */
/* ************************************************************************** */
void	*ft_free_set(void **ptr, void *set)
{
	if (ptr == NULL)
		return (NULL);
	free(*ptr);
	*ptr = set;
	return (*ptr);
}
