#include "ft_mem.h"

/* ************************************************************************** */
/*  This function sets 0 of n Bytes starting the pointer s.                   */
/* ************************************************************************** */
void	ft_bzero(void *s, size_t n)
{
	(void)ft_memset(s, 0, n);
}
