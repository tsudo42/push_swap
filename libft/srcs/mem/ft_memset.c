#include "ft_mem.h"

/* ************************************************************************** */
/*  This function sets len Bytes of (unsigned char)c starting from pointer b  */
/* ************************************************************************** */
void	*ft_memset(void *b, int c, size_t len)
{
	while (len-- > 0)
		((unsigned char *)b)[len] = (unsigned char)c;
	return (b);
}
