#include "ft_string.h"

/* ************************************************************************** */
/*  This funciton trims the trailing newlines('\n').                          */
/* ************************************************************************** */
char	*ft_trimnl(char *str)
{
	size_t	len;

	len = ft_strlen(str);
	while (len > 0)
	{
		len--;
		if (str[len] != '\n')
			break ;
		str[len] = '\0';
	}
	return (str);
}
