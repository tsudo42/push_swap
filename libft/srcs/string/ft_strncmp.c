#include "ft_string.h"

/* ************************************************************************** */
/*  This function compares string s1 and s2, at most n characters.            */
/* ************************************************************************** */
int	ft_strncmp(const char *s1, const char *s2, size_t n)
{
	size_t	i;

	i = 0;
	while (i < n)
	{
		if (s1[i] == '\0' || s1[i] != s2[i])
			return ((unsigned char)(s1[i]) - (unsigned char)(s2[i]));
		i++;
	}
	return (0);
}
