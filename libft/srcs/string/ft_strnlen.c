#include "ft_string.h"

/* ************************************************************************** */
/*  This function returns min(strlen(s), maxlen).                             */
/* ************************************************************************** */
size_t	ft_strnlen(const char *s, size_t maxlen)
{
	size_t	i;

	i = 0;
	while (i < maxlen)
	{
		if (s[i] == '\0')
			return (i);
		i++;
	}
	return (i);
}
