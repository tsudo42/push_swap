#include "ft_char.h"

/* ************************************************************************** */
/*  This function lowercases c if c is uppercase alphabet.                    */
/*  Otherwise, do nothing to input.                                           */
/* ************************************************************************** */
int	ft_tolower(int c)
{
	if ('A' <= c && c <= 'Z')
		c += ('a' - 'A');
	return (c);
}
