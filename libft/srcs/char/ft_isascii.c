#include "ft_char.h"

/* ************************************************************************** */
/*  This function returns if #arg1 is an ascii character.                     */
/*  Ascii characters are 0 ~ 127.                                             */
/* ************************************************************************** */
int	ft_isascii(int c)
{
	return (0x00 <= c && c <= 0x7F);
}
