#include "ft_char.h"

/* ************************************************************************** */
/*  This function returns if #arg1 is an alphabet character.                  */
/*  65 ~ 91 -> UPPERCASE, 97 -> 122 lowercase.                                */
/* ************************************************************************** */
int	ft_isalpha(int c)
{
	return (('A' <= c && c <= 'Z') || ('a' <= c && c <= 'z'));
}
