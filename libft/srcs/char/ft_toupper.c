#include "ft_char.h"

/* ************************************************************************** */
/*  This function uppercases c if c is lowercase alphabet.                    */
/*  Otherwise, do nothing to input.                                           */
/* ************************************************************************** */
int	ft_toupper(int c)
{
	if ('a' <= c && c <= 'z')
		c -= ('a' - 'A');
	return (c);
}
