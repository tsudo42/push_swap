#include "ft_char.h"

/* ************************************************************************** */
/*  This function returns if #arg1 is a number or an alphabet character.      */
/*  48 ~ 57 -> number, 65 ~ 90 -> UPPERCASE, 97 ~ 122 -> lowercase.           */
/* ************************************************************************** */
int	ft_isalnum(int c)
{
	return (('0' <= c && c <= '9') \
		|| ('A' <= c && c <= 'Z') || ('a' <= c && c <= 'z'));
}
