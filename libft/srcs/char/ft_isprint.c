#include "ft_char.h"

/* ************************************************************************** */
/*  This function returns if #arg1 is a printable character.                  */
/*  32 means ' '(sp) and 126 means '~'.                                       */
/* ************************************************************************** */
int	ft_isprint(int c)
{
	return (' ' <= c && c <= '~');
}
